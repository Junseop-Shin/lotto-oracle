/**
 * three/machine.js — 아케이드 기계 본체 (캐비닛) 3D 오브젝트
 *
 * 이 파일은 기계의 뼈대(몸통, 기둥, 발, 장식 띠)를 만들어요.
 * 스크린·버튼·레버 같은 인터랙티브 요소는 각자 파일에서 따로 만들어요.
 *
 * 구조:
 *   body      — 메인 박스 (크림색 둥근 직육면체)
 *   topBump   — 상단 돌출 장식
 *   pillars   — 좌우 기둥 (입체감)
 *   feet      — 하단 받침대 2개
 *   bands     — 파스텔 색상 가로 띠 3개 (핑크/블루/민트)
 */

import * as THREE from 'three';
import { toon, rbox, add } from './helpers.js';

export function buildMachine() {
  // ── 메인 본체 ────────────────────────────────────────────────────────
  // rbox(너비, 높이, 깊이, 모서리반경, 세그먼트)
  // shadow=true → 빛을 받아 다른 물체에 그림자를 드리움
  add(rbox(3.6, 5.0, 1.6, 0.28, 4), toon(0xe8d5c4), 0, 0, 0, true);

  // ── 상단 돌출 범프 ────────────────────────────────────────────────────
  add(rbox(2.8, 0.5, 1.3, 0.18, 3), toon(0xddc5b4), 0, 2.72, 0, true);

  // ── 좌우 세로 기둥 ────────────────────────────────────────────────────
  // forEach로 좌(-1.88)·우(+1.88) 대칭으로 생성
  [-1.88, 1.88].forEach(x =>
    add(rbox(0.22, 4.8, 1.62, 0.1, 2), toon(0xd4c0ae), x, 0, 0, true)
  );

  // ── 하단 발 2개 ───────────────────────────────────────────────────────
  [-1.5, 1.5].forEach(x =>
    add(rbox(0.55, 0.22, 1.42, 0.08, 2), toon(0xc4a898), x, -2.61, 0, true)
  );

  // ── 파스텔 가로 장식 띠 ───────────────────────────────────────────────
  // BoxGeometry(너비, 높이, 깊이) — 얇은 띠이므로 둥근 모서리 불필요
  [
    [0xffb3d9, 0, 2.45],   // 상단: 핑크
    [0xaacfff, 0, 0.1],    // 중단: 블루
    [0xa7f5ce, 0, -1.92],  // 하단: 민트
  ].forEach(([color, x, y]) =>
    add(new THREE.BoxGeometry(3.62, 0.09, 1.62), toon(color), x, y, 0)
  );
}
